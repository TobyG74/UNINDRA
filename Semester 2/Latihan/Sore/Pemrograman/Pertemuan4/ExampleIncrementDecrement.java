class ExampleIncrementDecrement {
    public static void main (String[]args){

        int z=10;
        System.out.println(++z); //increment penaikan 1
        System.out.println();
        
        int p=10;
        System.out.println(p++);
        System.out.println();
        
        int a=10;
        System.out.println(a--); //decrement penurunan 1
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println();
        
        int b=10;
        System.out.println(--b); //10-1=9 10 dikurangi 1 diceta  
        System.out.println();
        
        int c=10;
        System.out.println(c--); //10-1=9 dicetak 10, disimpan 9
        System.out.println(--c); //9-1= 8 9 dikurangi 1 dicetak 8
        System.out.println();
        
    }
}