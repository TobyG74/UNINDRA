    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if (inputKodeAlat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tidak Dapat Mengubah Data Kamu Belum Mengisi Kode Alat!");
        }
        String sql = "SELECT * FROM alat_camping WHERE kode_alat=?";
        try (Connection conn = getConnection();
            PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, inputKodeAlat.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                sql =  "UPDATE alat_camping SET nama_alat=?, merk=?, kondisi=?, kelengkapan=?, harga_sewa=?, stok=? WHERE kode_alat=?";
                PreparedStatement updatePs = conn.prepareStatement(sql);
                updatePs.setString(1, inputNamaAlat.getText().isEmpty() ? rs.getString("nama_alat") : inputNamaAlat.getText());
                updatePs.setString(2, inputMerkAlat.getText().isEmpty() ? rs.getString("merk") : inputMerkAlat.getText());
                updatePs.setString(3, inputKondisiAlat.getText().isEmpty() ? rs.getString("kondisi") : inputKondisiAlat.getText());
                updatePs.setString(4, inputKelengkapanAlat.getText().isEmpty() ? rs.getString("kelengkapan") : inputKelengkapanAlat.getText());
                updatePs.setString(5, inputHargaSewaAlat.getText().isEmpty() ? rs.getString("harga_sewa") : inputHargaSewaAlat.getText());
                updatePs.setString(6, inputStokAlat.getText().isEmpty() ? rs.getString("stok") : inputStokAlat.getText());
                updatePs.setString(7, inputKodeAlat.getText().toUpperCase()); // Primary Key
                updatePs.executeUpdate();
                loadDataTable();
                clearAllInput();
                JOptionPane.showMessageDialog(null, "Data Dengan Kode : " + inputKodeAlat.getText().toUpperCase() + " Berhasil Di Update!");
            } else {
                JOptionPane.showMessageDialog(null, "Data Alat Dengan Kode : " + inputKodeAlat.getText().toUpperCase() + " Tidak Terdaftar!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan Saat Pencarian Database :\n" + e.getMessage());
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if (inputKodeAlat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tidak Dapat Menyimpan Data Kamu Belum Mengisi Kode Alat!");
        }
        String sql = "SELECT * FROM alat_camping WHERE kode_alat=?";
        try (Connection conn = getConnection();
            PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, inputKodeAlat.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Data Alat Dengan Kode : " + inputKodeAlat.getText().toUpperCase() + " Sudah Terdaftar!");
            } else {
                sql = "INSERT INTO alat_camping (kode_alat, nama_alat, merk, kondisi, kelengkapan, harga_sewa, stok) VALUES " +
                        "(?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement insertPs = conn.prepareStatement(sql);
                insertPs.setString(1, inputKodeAlat.getText().toUpperCase()); // Primary Key
                insertPs.setString(2, inputNamaAlat.getText());
                insertPs.setString(3, inputMerkAlat.getText());
                insertPs.setString(4, inputKondisiAlat.getText());
                insertPs.setString(5, inputKelengkapanAlat.getText());
                insertPs.setString(6, inputHargaSewaAlat.getText());
                insertPs.setString(7, inputStokAlat.getText());
                insertPs.executeUpdate();
                loadDataTable();
                clearAllInput();
                JOptionPane.showMessageDialog(null, "Data Dengan Kode : " + inputKodeAlat.getText().toUpperCase() + " Berhasil Di Simpan!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan Saat Pencarian Database :\n" + e.getMessage());
        }
    }//GEN-LAST:event_saveButtonActionPerformed

