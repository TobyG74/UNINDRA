    private static Connection getConnection() {
        String URL = "jdbc:mysql://localhost:3306/camping";
        String USER = "root";
        String PASSWORD = "";
        try {
            if (connection == null || connection.isClosed()) {
                Class.forName("com.mysql.cj.jdbc.Driver");
                connection = DriverManager.getConnection(URL, USER, PASSWORD);
                System.out.println("Berhasil Connect ke Database!");
            }
        } catch (ClassNotFoundException | SQLException e) {
            System.err.println("Database connection error: " + e.getMessage());
        }
        return connection;
    }
    
    private void loadDataTable() {
        DefaultTableModel model = (DefaultTableModel) tableAlatCamping.getModel();
        model.setRowCount(0); 

        String sql = "SELECT * FROM alat_camping";
        try (Connection conn = getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                Object[] row = new Object[]{
                    rs.getString("kode_alat"),
                    rs.getString("nama_alat"),
                    rs.getString("merk"),
                    rs.getString("kondisi"),
                    rs.getString("kelengkapan"),
                    rs.getString("harga_sewa"),
                    rs.getString("stok")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat data tabel: " + e.getMessage());
        }
    }

